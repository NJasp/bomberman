void init_OutsideWalls(uint8_t grid[16][12])
{
	uint8_t collumn, row;
	for (row = 0; row < 12; row++) {
		for (collumn = 0; collumn < 16; collumn++) {
			if (row == 0 || row == 11) {
				grid[collumn][row] = 1;
			}
			else {
				grid[0][row] = 1;
				grid[15][row] = 1;
			}
		}
	}
}